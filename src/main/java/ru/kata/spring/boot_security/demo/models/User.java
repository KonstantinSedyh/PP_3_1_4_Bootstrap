package ru.kata.spring.boot_security.demo.models;

import org.hibernate.annotations.Fetch;
import org.hibernate.annotations.FetchMode;
import org.springframework.security.core.GrantedAuthority;
import org.springframework.security.core.userdetails.UserDetails;

import javax.persistence.*;
import javax.validation.constraints.Email;
import javax.validation.constraints.Min;
import javax.validation.constraints.NotEmpty;
import javax.validation.constraints.Size;
import java.util.ArrayList;
import java.util.Collection;
import java.util.List;

@Entity
@Table(name = "users")
public class User implements UserDetails {

    @Id
    @Column(name = "id")
    @GeneratedValue(strategy = GenerationType.IDENTITY)
    private Integer id;
    @NotEmpty(message = "Username should not be empty")
    @Size(min = 2, max = 15, message = "Incorrect username (2 - 15 symbols)")
    @Column(name = "username")
    private String username;

    @NotEmpty(message = "Lastname should not be empty")
    @Size(min = 2, max = 15, message = "Incorrect lastname (2 - 15 symbols)")
    @Column(name = "lastname")
    private String lastName;

    @NotEmpty(message = "Password should not be empty")
    @Size(min = 2, max = 500, message = "Incorrect password (2 - 500 symbols)")
    @Column(name = "password")
    private String password;

    @Min(value = 0, message = "Incorrect age (should be greater than 0)")
    @Column(name = "age")
    private Integer age;

    @NotEmpty(message = "Email should not be empty")
    @Email(message = "Incorrect email")
    @Column(name = "email")
    private String email;

    @ManyToMany(fetch = FetchType.LAZY)
//    @Fetch(FetchMode.JOIN)
    @JoinTable(
            name = "users_roles",
            joinColumns = @JoinColumn(name = "user_id"),
            inverseJoinColumns = @JoinColumn(name = "role_id")
    )
    private List<Role> userRoles = new ArrayList<>();

    public User() {
    }

    public User(String username, String password, Integer age, String email) {
        this.username = username;
        this.password = password;
        this.age = age;
        this.email = email;
    }

    public List<Role> getUserRoles() {
        return userRoles;
    }

    public void setUserRoles(Role userRole) {
        this.userRoles.add(userRole);
    }

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getUsername() {
        return username;
    }

    public void setUsername(String username) {
        this.username = username;
    }

    public String getLastName() {
        return lastName;
    }

    public void setLastName(String lastName) {
        this.lastName = lastName;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }

    public Integer getAge() {
        return age;
    }

    public void setAge(Integer age) {
        this.age = age;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    @Override
    public String toString() {
        return "User{" +
                "username='" + username + '\'' +
                ", password='" + password + '\'' +
                ", age=" + age +
                ", email='" + email + '\'' +
                '}';
    }

    public boolean hasRole(String roleName) {
        if (userRoles.stream()
                .map(Role::getName)
                .anyMatch(r -> r.equals(roleName))) {
            return true;
        }
        return false;
    }

    @Override
    public Collection<? extends GrantedAuthority> getAuthorities() {
        return null;
    }

    @Override
    public boolean isAccountNonExpired() {
        return true;
    }

    @Override
    public boolean isAccountNonLocked() {
        return true;
    }

    @Override
    public boolean isCredentialsNonExpired() {
        return true;
    }

    @Override
    public boolean isEnabled() {
        return true;
    }
}
